## 3-8. Seeing the World

visit_places = ["paris","london","ladakh","andaman","maldives"]
print(visit_places)
# Temporarily sort the places
print(sorted(visit_places))
print(visit_places)
# Temporarily sort in reverse
print(sorted(visit_places,reverse=True))
print(visit_places)
# Reverse the list
visit_places.reverse()
print(visit_places)
# Reverse again to bring it to original list
visit_places.reverse()
print(visit_places)
# Permanently Sort alphabetically
visit_places.sort()
print(visit_places)
# Permanently sort in reverse alphabetically
visit_places.sort(reverse=True)
print(visit_places)
